import turtle as t
t.setup(width=1200, height=800)
t.bgcolor('skyblue')
t.speed(0)
t.colormode(255)
t.hideturtle()


def draw_circle(x, y, r, color):
    t.penup()
    t.goto(x, y - r)
    t.pendown()
    t.fillcolor(color)
    t.begin_fill()
    t.circle(r)
    t.end_fill()

def draw_mountain(x, y, width, height, color):
    t.penup()
    t.goto(x, y)
    t.pendown()
    t.fillcolor(color)
    t.begin_fill()
    t.goto(x + width // 2, y + height)
    t.goto(x + width, y)
    t.goto(x, y)
    t.end_fill()

def draw_rectangle(x, y, w, h, color):
    t.penup()
    t.goto(x, y)
    t.pendown()
    t.fillcolor(color)
    t.begin_fill()
    for _ in range(2):
        t.forward(w)
        t.left(90)
        t.forward(h)
        t.left(90)
    t.end_fill()

def draw_tree(x, y, size=100):
    draw_rectangle(x, y, size//5, size, "saddlebrown")
    draw_circle(x + size//10, y + size, size//2, "forestgreen")
    draw_circle(x - size//4, y + size//2, size//3, "forestgreen")
    draw_circle(x + size//2, y + size//2, size//3, "forestgreen")

